public class Cliente {
    private String name;
    private int conta;
    private int agencia;


    public String getName() {
        return name;
    }

    public int getConta() {
        return conta;
    }

    public int getAgencia() {
        return agencia;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setConta(int conta) {
        this.conta = conta;
    }

    public void setAgencia(int agencia) {
        this.agencia = agencia;
    }

}
